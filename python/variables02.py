#------------------------------ Variables --------------------------------#
# no need to define type of variable in python
# variables are declared at time of initialisation
# variable name should contain alphabets, digits, underscores


# -----------numeric data types--------------#

x = 32                              # int 
pi = 3.14                           # float    
y = 2 + 3j                          # complex



# -----------boolean data type---------------#
z = True                            # bool (True or False)



# ----------sequence data types---------------#

name = "Lakshya"                                                    # string    
lst = [1, 3, "lakshya", True, name, 3.21]                           # list
tup = (11, 3.14, "poorab", "rohan", "priyanshu", "palash")          # tuple


# ---------mapping type----------------------#

dic = {"name": "Lakshya", "age": 32}                                # dictionary


# ------------------ set ---------------------#

s = {1,2,3}                                                         # set

