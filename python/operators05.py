# Operators

#___________________________________________ ARITHMETIC OPERATORS ______________________________________#

print (2 + 3)        # addition
print (3 - 2)        # substraction
print (2 * 3)        # multiplication
print (4 / 3)        # division
print (4 // 3)       # integer division
print (4 % 3)        # modulo (remainder)
print (2 ** 4)       # exponention



# _______________________________________ RELATIONAL OPERATORS __________________________________________#

print (2 == 2)      # equal to
print (2 != 3)      # not equal to
print (3 > 2)       # greater than
print (4 >= 2)      # greater than or equal to
print (4 < 2)       # less than
print (3 <= 4)      # less than or equal to



# _____________________________________ LOGICAL OPERATORS ____________________________________________#

print (True and False)      # logical and 
print (True or False)       # logical or
print (not True)            # logical not


# ____________________________________ MEMBERSHIP OPERATORS __________________________________________#

print ('x' in "abcdefghijkhmnopqrstuvwxyz")     # true if present
print (2 not in [2, 3, 3, 21, 12])              # true if not present

